import JWT from "jsonwebtoken";
import userModel from "../models/userModel.js";
import { hashPassword, compare } from "./../helper/authHelper.js";

export const registerController = async (req, res) => {
  try {
    const { name, email, password, phone, address, answer } = req.body;
    //validations
    if (!name) return res.send({ error: "name is required" });
    if (!email) return res.send({ error: "name is email" });
    if (!password) return res.send({ error: "name is password" });
    if (!phone) return res.send({ error: "name is phone" });
    if (!address) return res.send({ error: "name is address" });
    if (!answer) return res.send({ error: "name is answer" });

    //already exist
    const user = await userModel.findOne({ email });
    //existing user
    if (user) {
      return res.status(200).send({
        success: true,
        message: "user already exist",
      });
    }
    //if doesnt exist
    const hasedPassword = await hashPassword(password);
    //making object ready to save
    const newUser = await new userModel({
      name,
      email,
      password: hasedPassword,
      phone,
      address,
      answer,
    });
    await newUser.save();
    res.status(201).send({
      success: true,
      message: "user register successfully",
      newUser,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      message: "error in registration",
      error,
    });
  }
};

//Login

export const loginController = async (req, res) => {
  try {
    const { email, password } = req.body;
    //validation
    if (!email || !password) {
      return res.status(404).send({
        success: false,
        message: "Invalid email or password",
      });
    }
    //check user
    const user = await userModel.findOne({ email });
    if (!user) {
      return res.status(404).send({
        success: false,
        message: "Email is not registerd",
      });
    }
    const match = await compare(password, user.password);
    if (!match) {
      return res.status(200).send({
        success: false,
        message: "Invalid Password",
      });
    }
    //token
    const token = await JWT.sign({ _id: user._id }, process.env.SECRET_KEY, {
      expiresIn: "7d",
    });
    res.status(200).send({
      success: true,
      message: "login successfully",
      user: {
        _id: user._id,
        name: user.name,
        email: user.email,
        phone: user.phone,
        adddress: user.address,
        role: user.role,
      },
      token,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      message: "Error in login",
      error,
    });
  }
};

